interface ThemeCategory {
  theme: string;
  count: number;
  pct: number;
  examples: string[];
}

function categorizeYouthChanges(responses: string[]): ThemeCategory[] {
  const themes: Record<string, { count: number; examples: string[] }> = {
    "Kommunikáció fejlődése": { count: 0, examples: [] },
    "Kapcsolatok erősödése": { count: 0, examples: [] },
    "Nyitottság, elfogadás": { count: 0, examples: [] },
    "Magabiztosság, bátorság": { count: 0, examples: [] },
    "Filmezés iránti érdeklődés": { count: 0, examples: [] },
    "Csapatmunka fejlődése": { count: 0, examples: [] },
    "Jövőkép, célok": { count: 0, examples: [] },
    "Empátia növekedése": { count: 0, examples: [] },
    "Nem észlelt változást": { count: 0, examples: [] },
  };

  for (const r of responses) {
    const lower = r.toLowerCase();
    if (!r || lower.includes("nem tudom") || lower.includes("nemtudom") || lower.includes("semmiben") || lower.includes("nem volt") || lower === "nincs" || lower === "") {
      themes["Nem észlelt változást"].count++;
      continue;
    }
    let matched = false;
    if (lower.includes("kommunikáció") || lower.includes("beszél") || lower.includes("megszólal") || lower.includes("határozottabb")) {
      themes["Kommunikáció fejlődése"].count++; themes["Kommunikáció fejlődése"].examples.push(r); matched = true;
    }
    if (lower.includes("barát") || lower.includes("kapcsolat") || lower.includes("közelebb") || lower.includes("szorosabb")) {
      themes["Kapcsolatok erősödése"].count++; themes["Kapcsolatok erősödése"].examples.push(r); matched = true;
    }
    if (lower.includes("nyitott") || lower.includes("elfogad") || lower.includes("befogad") || lower.includes("idegen") || lower.includes("viszonyul")) {
      themes["Nyitottság, elfogadás"].count++; themes["Nyitottság, elfogadás"].examples.push(r); matched = true;
    }
    if (lower.includes("magabiztos") || lower.includes("bátr") || lower.includes("önbizalom") || lower.includes("félel")) {
      themes["Magabiztosság, bátorság"].count++; themes["Magabiztosság, bátorság"].examples.push(r); matched = true;
    }
    if (lower.includes("film") || lower.includes("videó") || lower.includes("kamera")) {
      themes["Filmezés iránti érdeklődés"].count++; themes["Filmezés iránti érdeklődés"].examples.push(r); matched = true;
    }
    if (lower.includes("csapat") || lower.includes("együttműködés") || lower.includes("közös munka")) {
      themes["Csapatmunka fejlődése"].count++; themes["Csapatmunka fejlődése"].examples.push(r); matched = true;
    }
    if (lower.includes("jövő") || lower.includes("cél") || lower.includes("továbbtanul") || lower.includes("hobbi")) {
      themes["Jövőkép, célok"].count++; themes["Jövőkép, célok"].examples.push(r); matched = true;
    }
    if (lower.includes("empátia") || lower.includes("érzelem") || lower.includes("örül") || lower.includes("megismer")) {
      themes["Empátia növekedése"].count++; themes["Empátia növekedése"].examples.push(r); matched = true;
    }
    if (!matched) {
      // Check for generic positive
      if (lower.includes("jobb") || lower.includes("fejlőd") || lower.includes("változ") || lower.includes("őszinté")) {
        themes["Magabiztosság, bátorság"].count++; themes["Magabiztosság, bátorság"].examples.push(r);
      }
    }
  }

  const total = responses.filter(r => r && r.length > 0).length;
  return Object.entries(themes)
    .map(([theme, data]) => ({
      theme,
      count: data.count,
      pct: Math.round((data.count / total) * 100),
      examples: data.examples.slice(0, 3),
    }))
    .sort((a, b) => b.count - a.count);
}

